public class PolyMorphismRecap {
    /*
    1.What is polymorphism
        *It is a different form of object, methods.(Poly-->many)

    2.What is dynamic and static polymorphism?
        *Dynamic polymorphism=> run time --> overriding
        *Static Polymorphism=> compile time -->overloading

    3.What is overloading and overriding?
        1.Overloading:
        *Must have different method signature(reference name plus parameters)
        *Overloading must have in same class
        *Overloading methods can have final, static, private(meaning return type and access modifiers can be different)





        2.Overriding:
        *Should have the same signature name
        *Overriding should be in different class, and mostly they are connected with extends keyword
        *You cannot override static, final, private methods
        SPECIAL NOTE: To be able to override we generally use Abstract and Interface.(With extends keyword)
                you can also override from object class(toString, Finalize)

     */
}
